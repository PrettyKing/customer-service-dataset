PREFIXES = [("", ""), ("换一种口语表达：", "您好，")]
MOCK = []
for instruction, user, assistant in SCENARIOS:
    for instruction_prefix, answer_prefix in PREFIXES:
        mock_user = user if not instruction_prefix else f"麻烦问下，{user}"
        MOCK.append(item(f"{instruction_prefix}{instruction}", mock_user, f"{answer_prefix}{assistant}"))


def write(name, records):
    (ROOT / name).write_text(json.dumps(records, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


